CREATE OR REPLACE PROCEDURE MM_ADD_AUTH_GROUP (
  AUTH_GROUP_NAME IN VARCHAR2,
  LOCALE_ZH_CN IN NVARCHAR2
)
IS
NEXT_ID NUMBER;
EXIST_COUNT NUMBER;
BEGIN
  SELECT MAX(ID)+1 INTO NEXT_ID FROM mm_auth_group;
  IF (NEXT_ID IS NULL) THEN
    NEXT_ID:=1;
  END IF;
  SELECT COUNT(ID) INTO EXIST_COUNT FROM mm_auth_group WHERE name=AUTH_GROUP_NAME;
  IF (EXIST_COUNT > 0) THEN
  	dbms_output.put_line('Same group exist');
  END IF;
  INSERT INTO mm_auth_group (id, name, locale_zh_cn) VALUES(NEXT_ID, AUTH_GROUP_NAME,LOCALE_ZH_CN);  
END;
